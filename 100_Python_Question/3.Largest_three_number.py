a = int(input("Enter the First number: "))
b = int(input("Enter the Second number: "))
c = int(input("Enter the Third number: "))

#using Conditional Statements
if(a > b and a > c):
    print("a is Largest",a)

elif(b > c):
    print("b is Largest",b)

else:
    print("c is Largest",c)

